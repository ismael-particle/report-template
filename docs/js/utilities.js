function array_scaling_factor(factor,column_factor,column_propierty_filter,value_filter,object_array){
    let object_array_copy =  JSON.parse(JSON.stringify(object_array)); 
    let month_count = 1;

    object_array_copy.forEach(element => {
        if ( element[column_propierty_filter] == value_filter) {
            element[column_factor] = Math.round( element[column_factor]*( 1+factor*(month_count/object_array_copy.length) ) );
            month_count++;
        }    
    });
    return object_array_copy;
}

function found_intersection (column_propierty,value, column_date, object_array){
    let data_lenght = 999999;
    let best_index = null;

    object_array.forEach((element,index) => {
        let distance = Math.abs(  element[column_propierty] - value );
        if ( distance < data_lenght) {
            best_index = index;
            data_lenght = distance;
        } 
    })
    if ( best_index == object_array.length-1) {
        return "Not found";
    }else{
        return (object_array[best_index][column_date]).substring(0,10);
    }
    return "Error...";
}

