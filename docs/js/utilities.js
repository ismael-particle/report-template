function calculate_package(monthly_data, column_average){
    //limits per year per device, in EP2023 PRO plan
    const package_limits =[
        [96000,'Package 1 - EP2023 PRO plan (96,000 data ops/year)'], //0
        [150000,'Package 2 - EP2023 PRO plan (150,000 data ops/year)'],
        [300000,'Package 3 - EP2023 PRO plan (300,000 data ops/year)'],
        [450000,'Package 4 - EP2023 PRO plan (450,000 data ops/year)'],
        [600000,'Package 5 - EP2023 PRO plan (600,000 data ops/year)'],
        [900000,'Package 6 - EP2023 PRO plan (900,000 data ops/year)'],
        [1200000,'Package 7 - EP2023 PRO plan (1,200,000 data ops/year)'],
        [1500000,'Package 8 - EP2023 PRO plan (1,500,000 data ops/year)'] //7
    ]

    var average_calculated = 0;
    var start_index = monthly_data.length<6?0:monthly_data.length-6; 

    for (i = start_index; i< monthly_data.length; i++) { 
        average_calculated += monthly_data[i][column_average];
    }
    average_calculated = average_calculated/(start_index<6?monthly_data.length:6);

    var index_selected;

    for (i = 0; i< package_limits.length; i++) { 
        index_selected = i;
        if (package_limits[i][0] > average_calculated) {
            break;
        }
    }
    var range_selected;

    switch (index_selected) {
        case 0:
            range_selected = 
                [
                    [package_limits[index_selected][0], package_limits[index_selected+1][0] ],
                    [package_limits[index_selected][1], package_limits[index_selected+1][1]]
                ];
            break;
        case (package_limits.length-1):
            range_selected = 
                [
                    [package_limits[index_selected-1][0], package_limits[index_selected][0]],
                    [package_limits[index_selected-1][1], package_limits[index_selected][1]]
                ];    
            break;
        default:
            range_selected = 
                [
                    [package_limits[index_selected-1][0], package_limits[index_selected][0], package_limits[index_selected+1][0] ],
                    [package_limits[index_selected-1][1], package_limits[index_selected][1], package_limits[index_selected+1][1] ],
                ];
            break;
    }
    return range_selected;
}

