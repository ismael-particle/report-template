function line_projection( data_list, qyt_proyected,ax_y,ax_x ){
    let source_data = [];
    let new_data_list = [];

    let start_array = (data_list.length <= 12 ) ? 0 : (data_list.length - 12);

    for (let i = start_array; i < data_list.length; i++) {
        source_data.push( [parseInt(i), data_list[i][ax_y]] )
        new_data_list.push( {
            date: data_list[i][ax_x],
            value: data_list[i][ax_y],
            source: 'fact'
        })
    }

    const result = regression.linear( source_data, {order: 2, precision: 2} );
    const gradient = result.equation[0];
    const yIntercept = result.equation[1];
    const last_date = new_data_list[ new_data_list.length - 1].date

    for (let i = 0; i < qyt_proyected; i++) {
        new_data_list.push(
            {
                date: addMonths(
                        new Date(last_date)
                        , i + 1),
                value: Math.round( (data_list.length - 1 + i)*gradient + yIntercept),
                source: 'projection'
            }
        )
    }
    return new_data_list
}

