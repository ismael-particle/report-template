//object A should be always metered object
function combine_objects(objectA, objectB, commonA_data, commonB_data, saveA, saveB){
    let shortObject;
    let shortCommon_data;
    let shortSave_data;

    let longObject;
    let longCommon_data;
    let longSave_data;

    if (objectA.length >= objectB.length) {
        shortObject = objectB;
        shortCommon_data = commonB_data
        shortSave_data = saveB

        longObject = objectA;
        longCommon_data = commonA_data;
        longSave_data = saveA;
    } else {
        shortObject = objectA;
        shortCommon_data = commonA_data
        shortSave_data = saveA

        longObject = objectB;
        longCommon_data = commonB_data;
        longSave_data = saveB;    
    }
    let new_object = [];

    let shortAverage = 0;
    shortObject.forEach(function(item,index, array){
        shortAverage = shortAverage + item[shortSave_data]
    })
    shortAverage = shortAverage/shortObject.length


    let longAverage = 0;
    longObject.forEach(function(item,index, array){
        longAverage = longAverage + item[longSave_data]
    })
    longAverage = longAverage/longObject.length

    shortObject.forEach(function(item,index, array){
        let same_element = longObject.find( 
            function (element) {
                let A = element[longCommon_data].trim().substring(0, 7);
                let B = item[shortCommon_data].trim().substring(0, 7);
                return A == B;
        })
        if (same_element) {
            let percentage_value = 0;

            if (shortAverage >= longAverage) {
                percentage_value = (( same_element[longSave_data]/item[shortSave_data] ) *100).toFixed(2)
            } else {
                percentage_value = (( item[shortSave_data]/same_element[longSave_data] ) *100).toFixed(2)   
            }

            if (objectA.length >= objectB.length) {
                new_object.push({
                    date: item[shortCommon_data],
                    data_A: same_element[longSave_data],
                    data_B: item[shortSave_data],
                    percentage: percentage_value
                })
            }else{
                new_object.push({
                    date: item[shortCommon_data],
                    data_A: item[shortSave_data],
                    data_B: same_element[longSave_data],
                    percentage: percentage_value
                })
            }
        }
    });	
    return new_object;
}

