function addMonths(date, months) {
    var d = date.getDate();
    date.setMonth(date.getMonth() +months);
    if (date.getDate() != d) {
      date.setDate(0);
    }
    date =  new Date(date).toISOString();
    return date;
}

function simple_clasification(object, x_value, limit, 
    porcentage = [0.80,0.90,1.0], 
    groups_name = ['clear_solid','warning_solid','minor_solid','major_solid']){
    
    let new_object = structuredClone(object)
    
    new_object.forEach(function(item,index, array){
        let porc = item[x_value] / limit;
        
        switch (true) {
            case (porc <= porcentage[0]):
                item['COLOR_STATE'] = groups_name[0];
                break;
            case (porc > porcentage[0] && porc <= porcentage[1]):
                item['COLOR_STATE'] = groups_name[1];
                break;
            case (porc > porcentage[1] && porc <= porcentage[2]):
                item['COLOR_STATE'] = groups_name[2];
                break;
            case (porc > porcentage[2]):
                item['COLOR_STATE'] = groups_name[3];
                break;
            default:
                item['COLOR_STATE'] = 'major_solid';
                break;
        }
    })

    return new_object;
}

